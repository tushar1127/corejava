package java_code_2019;

public abstract class Arithmatic {

	public Arithmatic() { }
	
	long sum(long num1 ,long num2 ){
		return num1 + num2;
	}
	
	abstract long division(long num1 ,long num2 );
	/*{
		return num1 / num2;
	}*/
	
	long multiplication(long num1 ,long num2 ){
		return num1 / num2;
	}
	
}